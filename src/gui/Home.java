    public void fillMyRes(){
        
        
        PreparedStatement ps ;
        ResultSet rs ;
        String query = "SELECT * FROM `reservations` WHERE `mail` ="+"'"+SystemClass.loggedUserMail+"'";
        
        try {
            Connection con = databaseConnection.connection();
            ps=con.prepareStatement(query);
            String s = "";

            
            rs= ps.executeQuery();
            
            if(rs.next()){
                do{
                ArrayList alist = new ArrayList<>();
                alist.add(rs.getString("ename"));
                alist.add(rs.getDate("e_date"));
                
               
               
               for(int i =0; i<alist.size();i++){
                 s+="<html>"+alist.get(i)+" <br> <br>";
               }
               }while(rs.next());
               s=s+" </html> ";
               jLabel2.setText(s);
              
                
                }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
        
    
    }
    
    
