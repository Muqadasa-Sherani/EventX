    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        SimpleDateFormat st = new SimpleDateFormat("MM-dd-yyyy");
        int rowIndex = table.getSelectedRow();
        jLabel6.setText(table.getValueAt(rowIndex,1).toString());
        jLabel3.setText(table.getValueAt(rowIndex,2).toString());
        jLabel7.setText(st.format(table.getValueAt(rowIndex,3)));
        jLabel8.setText(table.getValueAt(rowIndex,4).toString());
        
        
        jLabel9.setText("<html><body><span style='text-decoration: line-through;'>"+
                table.getValueAt(rowIndex, 6).toString()+"</span></body></html>");
        double actual_price;
        
        actual_price=Double.parseDouble(table.getValueAt(rowIndex, 6).toString().split("₺")[0]);
       
        
        if(null!=SystemClass.guest){
            finalprice = SystemClass.guest.calculateDiscount(actual_price);
            jLabel10.setText(finalprice+"₺");
        }
        if (null!=SystemClass.student){
            finalprice = SystemClass.student.calculateDiscount(actual_price);
            jLabel10.setText(finalprice+"₺");
        }
        if(null!=SystemClass.instructor){
            finalprice = SystemClass.instructor.calculateDiscount(actual_price);
            jLabel10.setText(finalprice+"₺");
        }
        selectedEvent=(String) table.getValueAt(rowIndex, 1);
        selectedEventDate= (Date) table.getValueAt(rowIndex, 3);
 
    }//GEN-LAST:event_tableMouseClicked

